// This file implements printing of expressions.

package types

import (
	"fmt"
	"go/ast"
	"go/internal/typeparams"
	"strings"
)

// ExprString returns the (possibly shortened) string representation for x.
// Shortened representations are suitable for user interfaces but may not
// necessarily follow Go syntax.
func ExprString(x ast.Expr) string {
	var sb strings.Builder
	WriteExpr(&sb, x)
	return sb.String()
}

// WriteExpr writes the (possibly shortened) string representation for x to buf.
// Shortened representations are suitable for user interfaces but may not
// necessarily follow Go syntax.
func WriteExpr(sb *strings.Builder, x ast.Expr) {
	// The AST preserves source-level parentheses so there is
	// no need to introduce them here to correct for different
	// operator precedences. (This assumes that the AST was
	// generated by a Go parser.)

	switch x := x.(type) {
	default:
		sb.WriteString(fmt.Sprintf("(ast: %T)", x)) // nil, ast.BadExpr, ast.KeyValueExpr

	case *ast.Ident:
		sb.WriteString(x.Name)

	case *ast.Ellipsis:
		sb.WriteString("...")
		if x.Elt != nil {
			WriteExpr(sb, x.Elt)
		}

	case *ast.BasicLit:
		sb.WriteString(x.Value)

	case *ast.FuncLit:
		sb.WriteByte('(')
		WriteExpr(sb, x.Type)
		sb.WriteString(" literal)") // shortened

	case *ast.CompositeLit:
		WriteExpr(sb, x.Type)
		sb.WriteByte('{')
		if len(x.Elts) > 0 {
			sb.WriteString("â€¦")
		}
		sb.WriteByte('}')

	case *ast.ParenExpr:
		sb.WriteByte('(')
		WriteExpr(sb, x.X)
		sb.WriteByte(')')

	case *ast.SelectorExpr:
		WriteExpr(sb, x.X)
		sb.WriteByte('.')
		sb.WriteString(x.Sel.Name)

	case *ast.IndexExpr, *ast.IndexListExpr:
		ix := typeparams.UnpackIndexExpr(x)
		WriteExpr(sb, ix.X)
		sb.WriteByte('[')
		writeExprList(sb, ix.Indices)
		sb.WriteByte(']')

	case *ast.SliceExpr:
		WriteExpr(sb, x.X)
		sb.WriteByte('[')
		if x.Low != nil {
			WriteExpr(sb, x.Low)
		}
		sb.WriteByte(':')
		if x.High != nil {
			WriteExpr(sb, x.High)
		}
		if x.Slice3 {
			sb.WriteByte(':')
			if x.Max != nil {
				WriteExpr(sb, x.Max)
			}
		}
		sb.WriteByte(']')

	case *ast.TypeAssertExpr:
		WriteExpr(sb, x.X)
		sb.WriteString(".(")
		WriteExpr(sb, x.Type)
		sb.WriteByte(')')

	case *ast.CallExpr:
		WriteExpr(sb, x.Fun)
		sb.WriteByte('(')
		writeExprList(sb, x.Args)
		if x.Ellipsis.IsValid() {
			sb.WriteString("...")
		}
		sb.WriteByte(')')

	case *ast.StarExpr:
		sb.WriteByte('*')
		WriteExpr(sb, x.X)

	case *ast.UnaryExpr:
		sb.WriteString(x.Op.String())
		WriteExpr(sb, x.X)

	case *ast.BinaryExpr:
		WriteExpr(sb, x.X)
		sb.WriteByte(' ')
		sb.WriteString(x.Op.String())
		sb.WriteByte(' ')
		WriteExpr(sb, x.Y)

	case *ast.ArrayType:
		sb.WriteByte('[')
		if x.Len != nil {
			WriteExpr(sb, x.Len)
		}
		sb.WriteByte(']')
		WriteExpr(sb, x.Elt)

	case *ast.StructType:
		sb.WriteString("struct{")
		writeFieldList(sb, x.Fields.List, "; ", false)
		sb.WriteByte('}')

	case *ast.FuncType:
		sb.WriteString("func")
		writeSigExpr(sb, x)

	case *ast.InterfaceType:
		sb.WriteString("interface{")
		writeFieldList(sb, x.Methods.List, "; ", true)
		sb.WriteByte('}')

	case *ast.MapType:
		sb.WriteString("map[")
		WriteExpr(sb, x.Key)
		sb.WriteByte(']')
		WriteExpr(sb, x.Value)

	case *ast.ChanType:
		var s string
		switch x.Dir {
		case ast.SEND:
			s = "chan<- "
		case ast.RECV:
			s = "<-chan "
		default:
			s = "chan "
		}
		sb.WriteString(s)
		WriteExpr(sb, x.Value)
	}
}

func writeSigExpr(sb *strings.Builder, sig *ast.FuncType) {
	sb.WriteByte('(')
	writeFieldList(sb, sig.Params.List, ", ", false)
	sb.WriteByte(')')

	res := sig.Results
	n := res.NumFields()
	if n == 0 {
		// no result
		return
	}

	sb.WriteByte(' ')
	if n == 1 && len(res.List[0].Names) == 0 {
		// single unnamed result
		WriteExpr(sb, res.List[0].Type)
		return
	}

	// multiple or named result(s)
	sb.WriteByte('(')
	writeFieldList(sb, res.List, ", ", false)
	sb.WriteByte(')')
}

func writeFieldList(sb *strings.Builder, list []*ast.Field, sep string, iface bool) {
	for i, f := range list {
		if i > 0 {
			sb.WriteString(sep)
		}

		// field list names
		writeIdentList(sb, f.Names)

		// types of interface methods consist of signatures only
		if sig, _ := f.Type.(*ast.FuncType); sig != nil && iface {
			writeSigExpr(sb, sig)
			continue
		}

		// named fields are separated with a blank from the field type
		if len(f.Names) > 0 {
			sb.WriteByte(' ')
		}

		WriteExpr(sb, f.Type)

		// ignore tag
	}
}

func writeIdentList(sb *strings.Builder, list []*ast.Ident) {
	for i, x := range list {
		if i > 0 {
			sb.WriteString(", ")
		}
		sb.WriteString(x.Name)
	}
}

func writeExprList(sb *strings.Builder, list []ast.Expr) {
	for i, x := range list {
		if i > 0 {
			sb.WriteString(", ")
		}
		WriteExpr(sb, x)
	}
}
