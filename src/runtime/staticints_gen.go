// +build ignore

package main

import (
	"flag"
	"fmt"
	"log"
	"os"
)

func main() {
	flag.Parse()
	if flag.NArg() > 0 {
		f, err := os.Create(flag.Arg(0))
		if err != nil {
			log.Fatal(err)
		}
		os.Stdout = f
	}
	fmt.Printf("// generated by staticints_gen.go; use go generate\n\n")
	fmt.Printf("package runtime\n\n")
	fmt.Printf("var staticints = [...]uintptr{\n")
	for i := 0; i < 1024; i++ {
		fmt.Printf("\t%d,\n", i)
	}
	fmt.Printf("}\n")
}
